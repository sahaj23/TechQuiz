 private int validateInputs()
 {
      userid=txtUserid.getText();
      password=txtPassword.getText();
      retypePassword=txtRetypepassword.getText();
      //System.out.println(password+" "+retypePassword);
     if(userid.isEmpty()||password.isEmpty()||retypePassword.isEmpty())
        return 0;
     else if(userid.length()<4 || password.length()<4)
         return -1;
     else if(password.equals(retypePassword)==false)
         return -2;
     else
         return 1;
         
 }
