    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        
        int isValidInput=validateInputs();
        if(isValidInput==0)
        {
            JOptionPane.showMessageDialog(null,"Userid,Password or RetypePassword can not be left blank!!","Error!!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(isValidInput==-1)
        {
             JOptionPane.showMessageDialog(null,"Length of Userid and Password must be greater than 3!!","Error!!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(isValidInput==-2)
        {
             JOptionPane.showMessageDialog(null,"Password and RetypePassword must be same!!","Error!!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
            UserPojo user=new UserPojo(userid,password,"student");
            try{
       
            if(UserDAO.addUser(user))
            {
                JOptionPane.showMessageDialog(null,"Registration Successful!!","SUCCESS!!",JOptionPane.INFORMATION_MESSAGE);
             (new AdminOptionsFrame()).setVisible(true);
             this.dispose();
            }
            else
            {
                 JOptionPane.showMessageDialog(null,"Registration Rejected!!\nUserid already exists!","Error!!",JOptionPane.ERROR_MESSAGE);
                 clearAll();
                 return;
            }
            
            }
            catch(SQLException ex)
            {
                 ex.printStackTrace();
                 JOptionPane.showMessageDialog(null,"Some Error Occured in DB!!"+ex,"Exception!!",JOptionPane.ERROR_MESSAGE);
                 return;
            }
           }
        
    }//GEN-LAST:event_btnRegisterActionPerformed
    
